# Class is blueprint

class Employee():                      
    language="python"       # This is a class attribute        
    salary=10000                        

aadi=Employee()
aadi.name="Aadi"            # This an instace or object attribute
print(aadi.name,aadi.language,aadi.salary)

viren=Employee()
viren.name="Viren"          # This an instace or object attribute
print(viren.name,viren.salary,viren.language)



